//llama a la vista GuiPrincipal ,si la contrase単a es correcta
    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        // TODO add your handling code here:
        GestorParqueadero gestorPa = new GestorParqueadero();
        Parqueadero par = new Parqueadero();
        GestorPersona gestorPer = new GestorPersona();
        Persona persona = new Persona();

        String Pass = new String(jtxtFPassword.getPassword());
        try {
            persona = gestorPer.consultarPersona(jtxtFUser.getText(), Pass);
        } catch (Exception e) {
            System.out.println(e);
        }

        if (persona == null) {
            JOptionPane.showMessageDialog(null, "Usuario o contrase単a incorrectos");
            limpiar();
        } else {
            try {
                par = gestorPa.consultarParqueadero(jtxtFUser.getText());
            } catch (Exception e) {
                System.out.println(e);
            }
            if (par == null) {
                JOptionPane.showMessageDialog(null, "Usuario ingresado no esta relacionado con ningun Parqueadero");
                limpiar();
            } else {
                atrFuncionario = par.getUsuarioPar();
                atrNit = par.getNitParqueadero();
                atrNomParqueadero = par.getNomParqueadero();
                atrLibres = par.getPuestosLibres();
                GUIPaginaPrincipal ins = new GUIPaginaPrincipal();
                ins.setVisible(true);
                ins.setLocationRelativeTo(null);

                this.dispose();
            }

        }
    }//GEN-LAST:event_btnIngresarActionPerformed
