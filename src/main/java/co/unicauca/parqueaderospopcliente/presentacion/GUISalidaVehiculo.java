// calcula el tiempo que estuvo un vehiculo en el parqueadero
    private void jtxtTiempoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtxtTiempoMouseClicked
        // TODO add your handling code here:
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyy-MM-dd H:m:s");
        int diferencia;
        int dias = 0;
        int horas = 0;
        int minutos = 0;
        try {
            Date fechaInicial = dateFormat.parse(lblHoraYFechaEntrada.getText());
            Date fechaFinal = dateFormat.parse(lblHoraYFechaSalida.getText());
            diferencia = (int) ((fechaFinal.getTime() - fechaInicial.getTime()) / 1000);

            if (diferencia > 86400) {
                dias = (int) Math.floor(diferencia / 86400);
                diferencia = diferencia - (dias * 86400);
            }
            if (diferencia > 3600) {
                horas = (int) Math.floor(diferencia / 3600);
                diferencia = diferencia - (horas * 3600);
            }
            if (diferencia > 60) {
                minutos = (int) Math.floor(diferencia / 60);
                diferencia = diferencia - (minutos * 60);
            }
            
            jtxtTiempo.setText("Horas:" + horas + " " + "Minutos:"+minutos);
        } catch (ParseException ex) {
            Logger.getLogger(GUISalidaVehiculo.class.getName()).log(Level.SEVERE, null, ex);
        }
        atrHoras = horas;
        atrMinutos = minutos;

    }//GEN-LAST:event_jtxtTiempoMouseClicked
