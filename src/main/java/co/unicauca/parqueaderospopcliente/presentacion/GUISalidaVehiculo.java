//sacamos el valor a pagar del vehiculo si es carro o moto,ademas de un recargo si perdio su ficha 
    private void jtxtFValorPagarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtxtFValorPagarMouseClicked
        // TODO add your handling code here:
        int multa;
        int tarifaPagar;
        int valorPagar;
        String aHoras = Integer.toString(atrHoras);
        String aMinutos = Integer.toString(atrMinutos);
        GestorTarifa gestorT = new GestorTarifa();
        Tarifa tari = new Tarifa();
        if (jtxtFTipoVehiculo.getText().equals("Carro")) {
            try {
                tari = gestorT.consultarTarifa(aHoras, aMinutos);
                System.out.println("Consulta realizada");
            } catch (Exception e) {
                System.out.println(e);
            }
        } else {
            if (jtxtFTipoVehiculo.getText().equals("Moto")) {
                try {
                    tari = gestorT.consultarTarifaMotos(aHoras, aMinutos);
                    System.out.println("Consulta realizada");
                } catch (Exception e) {
                    System.out.println(e);
                }
            }
        }

        if (jRBPerdida.isSelected()) {
            multa = 5000;
            tarifaPagar = Integer.parseInt(tari.getValorTotal());
            valorPagar = multa + tarifaPagar;
            jtxtFValorPagar.setText("$" + valorPagar);
        } else {
            jtxtFValorPagar.setText("$" + " " + tari.getValorTotal());
        }
    }//GEN-LAST:event_jtxtFValorPagarMouseClicked

