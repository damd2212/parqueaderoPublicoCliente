    //Metodo que actualiza el estado y fecha de salida del vehiculo cuando se le de al boton retirar 
    private void btnRetirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRetirarActionPerformed
        
        GUIIniciarSesion iniciar = new GUIIniciarSesion();
        GestorRegVehiculo gestorR = new GestorRegVehiculo();
        GestorParqueadero gestorPar = new GestorParqueadero();
        String Placa = jtxtPlaca.getText();
        String NitParqueadero = iniciar.atrNit;
        String HoraFechaSalida = lblHoraYFechaSalida.getText();

        if (jtxtFValorPagar.getText().isEmpty() || jtxtTiempo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "No pueden quedar campos vacios");
        } else {
            try {
                gestorR.actualizarRegVehiculo(Placa, HoraFechaSalida);
                gestorPar.actualizarSalida(NitParqueadero);
                JOptionPane.showMessageDialog(null, "Vehiculo retirado");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error al retirar");
            }
            limpiar();
            dispose();
            GUIBuscarVehiculo bus = new GUIBuscarVehiculo();
            bus.setVisible(true);
            bus.setLocationRelativeTo(null);
        }
    }//GEN-LAST:event_btnRetirarActionPerformed

